package com.taikang.tkdoctor.base.requestback;

public class HealthRiskBean {
	
	private static final long serialVersionUID = 7322343273L;
	
	private String healthTag;
	private String healthContent;
	private String healthappraisal;
	private String healthRisks;
	public String getHealthTag() {
		return healthTag;
	}
	public void setHealthTag(String healthTag) {
		this.healthTag = healthTag;
	}
	public String getHealthContent() {
		return healthContent;
	}
	public void setHealthContent(String healthContent) {
		this.healthContent = healthContent;
	}
	public String getHealthappraisal() {
		return healthappraisal;
	}
	public void setHealthappraisal(String healthappraisal) {
		this.healthappraisal = healthappraisal;
	}
	public String getHealthRisks() {
		return healthRisks;
	}
	public void setHealthRisks(String healthRisks) {
		this.healthRisks = healthRisks;
	}
	
	
}
